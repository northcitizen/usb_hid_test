
#include "main.h"
#include "usb_device.h"

uint8_t usbReceiveData[12] = {0};
uint8_t USB_PACKET_RX = 0;

void SystemClock_Config(void);
static void MX_GPIO_Init(void);

int main(void)
{
  HAL_Init();

  SystemClock_Config();

  MX_GPIO_Init();
  MX_USB_DEVICE_Init();
  HAL_GPIO_WritePin(GPIOH, GPIO_PIN_4, RESET);
  while (1)
  {
		if (USB_PACKET_RX) {
			if (usbReceiveData[0] == 0xAB) {
				HAL_GPIO_WritePin(GPIOH, GPIO_PIN_4, SET);
			}
		}
	}

}


